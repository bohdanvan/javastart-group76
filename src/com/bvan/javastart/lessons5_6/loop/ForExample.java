/**
 * @author bvanchuhov
 */
public class ForExample {

    public static void main(String[] args) {
        // for (<1>; <2> ; <3>) { ... }

        for (int n = 10; n <= 20; n++) {
            System.out.print(n + " ");
        }
        System.out.println();
    }
}
